<p>Puro Beach Illetas is arguably the most iconic beach club on the southwest coast of Mallorca. Described as an "Oasis del Mar", it sits directly on the shore at Illetas with panoramic views across the Bay of Palma. The Balinese-inspired decor, resident DJs, and meticulously crafted cocktail menu create an experience that has earned Puro Beach a loyal international following.</p>

